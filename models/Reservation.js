const mongoose = require('mongoose');

const ReservationSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const Reservation = mongoose.model('Reservation', ReservationSchema);
module.exports = Reservation;
